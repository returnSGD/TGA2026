class TemplateLibrary:
    """
    心声模板库 —— LLM不可用时的降级方案。

    三层回退策略：
    1. 精确命中: cat_id + intent → 随机选择
    2. 通用回退: intent → GENERIC_TEMPLATES
    3. 绝对兜底: "……"
    """

    def __init__(self):
        self._templates = TEMPLATES
        self._generic = GENERIC_TEMPLATES
        self._usage_stats: Dict[str, int] = defaultdict(int)
        self._total_served = 0

    def get_template(self, cat_id: str, intent: str,
                     fear_bucket: str = "mid",
                     trust_bucket: str = "mid") -> str:
        """获取一条心声模板。

        Args:
            cat_id: 猫咪ID (xiaoxue/oreo/orange)
            intent: 当前意图
            fear_bucket: 恐惧值分桶 (very_low/low/mid/high/very_high)
            trust_bucket: 信任度分桶
        Returns:
            心声文本
        """
        self._total_served += 1

        # 第一层: cat_id + intent 精确匹配
        cat_templates = self._templates.get(cat_id, {})
        intent_templates = cat_templates.get(intent, [])

        if intent_templates:
            selected = random.choice(intent_templates)
            self._usage_stats[f"{cat_id}:{intent}"] += 1
            return selected

        # 第二层: intent 通用匹配
        generic = self._generic.get(intent, ["……"])
        selected = random.choice(generic)
        self._usage_stats[f"generic:{intent}"] += 1
        return selected

    def get_templates_batch(self, cat_id: str, intent: str,
                            count: int = 3) -> List[str]:
        """获取多条不重复的心声模板（用于预生成/离线评估）"""
        cat_templates = self._templates.get(cat_id, {})
        intent_templates = cat_templates.get(intent, [])

        if len(intent_templates) >= count:
            return random.sample(intent_templates, count)
        else:
            result = list(intent_templates)
            generic = self._generic.get(intent, ["……"])
            while len(result) < count:
                result.append(random.choice(generic))
            return result

    def get_template_for_emotion(self, cat_id: str, intent: str,
                                  fear: float, trust: float) -> str:
        """根据情绪值自动分桶后获取模板"""
        return self.get_template(
            cat_id, intent,
            fear_bucket=_bucket(fear),
            trust_bucket=_bucket(trust),
        )

    @property
    def total_templates(self) -> int:
        total = 0
        for cat in self._templates.values():
            for intent_tmpls in cat.values():
                total += len(intent_tmpls)
        return total

    @property
    def total_served(self) -> int:
        return self._total_served

    def stats(self) -> Dict:
        return {
            "total_templates": self.total_templates,
            "total_served": self._total_served,
            "top_used": sorted(self._usage_stats.items(),
                              key=lambda x: -x[1])[:10],
        }
